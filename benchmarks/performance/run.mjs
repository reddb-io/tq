#!/usr/bin/env node
/**
 * Runtime performance benchmark for the JS package.
 *
 * The third axis, alongside `accuracy/` (how well a model reads TOON) and
 * `tokens/` (how much a payload costs to send). This one answers: how long
 * does the shipped codec take, and how many bytes does it move?
 *
 * It measures the same four operations as the Rust criterion suite in
 * `crates/toon/benches/codec.rs`, over the same corpora, so the two sides are
 * read side by side:
 *
 *   encode        value -> TOON wire
 *   decode        TOON wire -> value
 *   json_to_toon  JSON text -> TOON wire
 *   toon_to_json  TOON wire -> JSON text
 *
 * Reported per case: median ms, ops/s, and the input/output byte sizes.
 * Timings are wall-clock and machine-specific — they are a baseline to compare
 * against on the same machine, not a number to publish. The CI guard against
 * quadratic regressions lives in the test suites, not here.
 */
import { readdirSync, readFileSync, statSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

import { decode, encode } from '../../packages/toon/src/index.js'

const REPO_ROOT = dirname(dirname(dirname(fileURLToPath(import.meta.url))))
const RESULTS_DIR = join(REPO_ROOT, 'benchmarks', 'results')
const CORPORA = [
  // The shared token corpus: representative payload shapes.
  join(REPO_ROOT, 'benchmarks', 'datasets'),
  // The performance axis' own pathological payload (see #194).
  join(REPO_ROOT, 'benchmarks', 'performance', 'datasets'),
]

const MIN_SAMPLES = 5
const MIN_TOTAL_MS = 400
const encoder = new TextEncoder()

function bytes(text) {
  return encoder.encode(text).length
}

function datasetFiles(dir, found = []) {
  for (const entry of readdirSync(dir).sort()) {
    const path = join(dir, entry)
    if (statSync(path).isDirectory()) datasetFiles(path, found)
    else if (entry.endsWith('.json')) found.push(path)
  }
  return found
}

function loadCases() {
  return CORPORA.flatMap((dir) => datasetFiles(dir)).map((file) => {
    const jsonText = readFileSync(file, 'utf8')
    const value = JSON.parse(jsonText)
    return {
      name: relative(REPO_ROOT, file).replace(/\.json$/, ''),
      jsonText,
      value,
      toonText: encode(value),
    }
  })
}

/**
 * Runs `work` until both sample and time floors are met, returning the median.
 * The median, not the mean: one GC pause during a run should not define the
 * number the next person compares against.
 */
function measure(work) {
  work() // Warm up, so the first sample is not paying for lazy compilation.
  const samples = []
  const started = performance.now()
  while (samples.length < MIN_SAMPLES || performance.now() - started < MIN_TOTAL_MS) {
    const iterationStart = performance.now()
    work()
    samples.push(performance.now() - iterationStart)
  }
  samples.sort((left, right) => left - right)
  return samples[Math.floor(samples.length / 2)]
}

const OPERATIONS = [
  {
    id: 'encode',
    inputBytes: (testCase) => bytes(testCase.jsonText),
    run: (testCase) => encode(testCase.value),
  },
  {
    id: 'decode',
    inputBytes: (testCase) => bytes(testCase.toonText),
    run: (testCase) => decode(testCase.toonText),
  },
  {
    id: 'json_to_toon',
    inputBytes: (testCase) => bytes(testCase.jsonText),
    run: (testCase) => encode(JSON.parse(testCase.jsonText)),
  },
  {
    id: 'toon_to_json',
    inputBytes: (testCase) => bytes(testCase.toonText),
    run: (testCase) => JSON.stringify(decode(testCase.toonText)),
  },
]

function formatMs(value) {
  if (value >= 100) return value.toFixed(0)
  if (value >= 1) return value.toFixed(2)
  return value.toFixed(3)
}

function throughput(inputBytes, medianMs) {
  return (inputBytes / 1024 / 1024 / (medianMs / 1000)).toFixed(1)
}

function report(cases) {
  const lines = [
    '# Runtime performance — JS (`@reddb-io/toon`)',
    '',
    'Generated by `pnpm benchmark:performance`.',
    '',
    'Wall-clock medians on the machine that produced this file. Compare against',
    'a re-run on *your* machine, never against a number from another one; see',
    '`benchmarks/README.md` for how to read this.',
    '',
    `- node: \`${process.version}\``,
    `- samples per case: at least ${MIN_SAMPLES}, over at least ${MIN_TOTAL_MS}ms`,
    '',
  ]

  for (const operation of OPERATIONS) {
    lines.push(`## ${operation.id}`, '')
    lines.push('| Dataset | Input bytes | Median ms | MiB/s | ops/s |')
    lines.push('| --- | ---: | ---: | ---: | ---: |')
    for (const testCase of cases) {
      const inputBytes = operation.inputBytes(testCase)
      const medianMs = measure(() => operation.run(testCase))
      lines.push(
        `| \`${testCase.name}\` | ${inputBytes} | ${formatMs(medianMs)} | ` +
          `${throughput(inputBytes, medianMs)} | ${(1000 / medianMs).toFixed(0)} |`,
      )
      process.stderr.write(`  ${operation.id} ${testCase.name}: ${formatMs(medianMs)}ms\n`)
    }
    lines.push('')
  }
  return lines.join('\n')
}

const cases = loadCases()
if (cases.length === 0) {
  console.error('No datasets found. Run `node scripts/generate_html_heavy_dataset.mjs` first.')
  process.exit(1)
}

process.stderr.write(`Measuring ${cases.length} datasets x ${OPERATIONS.length} operations...\n`)
const markdown = report(cases)

const writeTo = process.argv[2] ?? join(RESULTS_DIR, 'runtime-performance-js.md')
mkdirSync(dirname(writeTo), { recursive: true })
writeFileSync(writeTo, `${markdown}\n`)
process.stderr.write(`\nWrote ${relative(REPO_ROOT, writeTo)}\n`)
